如果 $ k + k^2 = n $ ，请求解 $ k $ 值

请证明勾股定理 $ a^2 + b^2 = c^2 $

请使用 mermaid 绘出证明过程


```mermaid
graph TD
    A[构造一个边长为 a+b 的大正方形] --> B[在大正方形的四个角放置四个直角三角形]
    B --> C[每个三角形的直角边为 a 和 b，斜边为 c]
    C --> D[中间形成一个边长为 c 的小正方形]
    D --> E[大正方形面积为 (a+b)^2]
    D --> F[四个三角形总面积为 2ab]
    D --> G[小正方形面积为 c^2]
    E --> H[建立等式: (a+b)^2 = 2ab + c^2]
    H --> I[化简得: a^2 + b^2 = c^2]
```

请注意，Mermaid 主要用于流程图和图表，而不是用于精确绘制几何图形。如果需要更精确的几何图形绘制，建议使用其他工具，如 LaTeX 的 TikZ 包。",
